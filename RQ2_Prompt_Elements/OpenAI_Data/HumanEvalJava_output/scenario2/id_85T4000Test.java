// Add2.java
package scenario2;

import java.util.List;

class Add2 {

    /**
     * Given a non-empty list of integers lst. add the even elements that are at odd indices..
     */
    public static int add(List<Integer> lst) {
        int result = 0;
        for (int i = 1; i < lst.size(); i += 2) {
            if (lst.get(i) % 2 == 0) {
                result += lst.get(i);
            }
        }
        return result;
    }
}

